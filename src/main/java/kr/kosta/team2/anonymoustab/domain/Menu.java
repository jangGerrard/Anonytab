package kr.kosta.team2.anonymoustab.domain;

public class Menu {
	private long code;
	private String name;
	private String url;
	
	public long getCode() {
		return code;
	}
	
	public void setId(long code) {
		this.code = code;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
